    {
        abort(404);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\TypeEntry  $type_entry
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, TypeEntry $type_entry)
    {
        $type_entry->description = $request['description'];
        $type_entry->save();
    }

